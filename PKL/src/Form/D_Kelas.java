    private void TampilData() {
        //membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        //model.addColumn("NO");
        model.addColumn("KODE KELAS");
        model.addColumn("NAMA KELAS");
        model.addColumn("WALI KELAS");
        tblkelas.setModel(model);

        //menampilkan data database kedalam tabel
        try {
            //int i=1;
            java.sql.Connection conn = new DBConnection().connect();
            java.sql.Statement stat = conn.createStatement();
            ResultSet data = stat.executeQuery("SELECT id_kelas, nama_kelas, wali_kelas FROM p_kelas");
            while (data.next()) {
                model.addRow(new Object[]{
                    //("" + i++),
                    data.getString("id_kelas"),
                    data.getString("nama_kelas"),
                    data.getString("wali_kelas")
                });
                tblkelas.setModel(model);
            }
        } catch (Exception e) {
            System.err.println("ERROR:" + e);
        }
    }
    
    private void cbwakel() {
        cbwakel.removeAllItems();
        cbwakel.addItem("Pilih Wali Kelas");
        try {
            java.sql.Connection conn = new DBConnection().connect();
            java.sql.Statement stat = conn.createStatement();
            String sql_tc = "select kd_guru, nama_guru from p_guru";
            ResultSet rs = stat.executeQuery(sql_tc);

            while (rs.next()) {
                String nama = rs.getString("nama_guru");
                cbwakel.addItem(nama);
            }
            rs.close();
            stat.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

