    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        java.sql.Connection conn = new DBConnection().connect();
        String id = txt_id.getText();
        String nama= txt_nama.getText();
        String wakel = (String) cbwakel.getSelectedItem();
        if (txt_id.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Tolong masukan data anda ", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                java.sql.Statement stat = conn.createStatement();
                ResultSet data = stat.executeQuery("SELECT * FROM p_kelas WHERE id_kelas ='" + txt_id.getText() + "'");
                if (data.next()) {
                    JOptionPane.showMessageDialog(null, "Kode Kelas telah terdaftar", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                    txt_id.requestFocus();
                } else {
                    String sql = "INSERT INTO p_kelas (id_kelas, nama_kelas, wali_kelas) VALUES('" + id + "'"
                    + ",'" + nama + "'"
                    + ",'" + wakel + "')";
                    stat.executeUpdate(sql);
                    kosong();
                    JOptionPane.showMessageDialog(null, "Penyimpanan Berhasil", "BERHASIL", JOptionPane.INFORMATION_MESSAGE);
                    TampilData();
                    //tampilmata_kuliah("SELECT * FROM p_siswa");
                }
                stat.close();
            } catch (Exception exc) {
                System.err.println("ERROR:" + exc);
            }
        }
    }//GEN-LAST:event_bsimpanActionPerformed

